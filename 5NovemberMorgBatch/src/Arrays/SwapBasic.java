package Arrays;

public class SwapBasic {
	public static void main(String[] args)
	{
		int a= 10;
		int b=56;
		
		System.out.println(a);
		System.out.println(b);
		
		System.out.println("Integer swapping");
		
		int c;
		c=a;
		a=b;
		b=c;
		
		System.out.println(a);
		System.out.println(b);
		}

}
