package AbstractionEncapsulationGeneralization;

public class Jio implements Simcard{
	
	public void Subscription()
	{
		System.out.println("Jio TV subscription");
	}
	
	public void TextMessage()
	{
		System.out.println("Daily 100 SMS");
	}
	
	public void AutoCalling()
	{
		System.out.println("Unlimited calling");
	}
	
	public void InternetData()
	{
		System.out.println("2GB per day");
	}
	

}
