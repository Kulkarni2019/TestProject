package Casting;

public class Upcasting { // super class
	
	public void set()
	{
		System.out.println("Non static super class set method is running");
	}

	public void show()
	{
		System.out.println("Super class show method is running");
	}
	
	public static void cast()
	{
		System.out.println("static method is running");
	}
}

	
